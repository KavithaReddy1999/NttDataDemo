package com.basics;

public class IncrementDecrementOperators {

	public static void main(String[] args) {

		int x=10;
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
		
		int a=10,b=10;
		System.out.println(a++ + ++a);
		System.out.println(b++ + b++);
	}

}
