package com.basics;

public class Variables {
	
	int a =45; //instance variables
	
	static float f=23.4f; //static variables
	

  public static void main(String[] args) {
	  
	  int n =34;//local variables
	  
	  System.out.println(n);
	  
	  //System.out.println(a); we can't access instance variables within static
	  
	  System.out.println(f);
	  System.out.println('e');

	}

}
