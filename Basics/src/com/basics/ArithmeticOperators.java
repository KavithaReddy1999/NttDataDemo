package com.basics;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a=20;
		int b=4;
		String s="kavitha";
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);//remainder
		System.out.println(s+a+b); // acts as concatenation
		
	}

}
